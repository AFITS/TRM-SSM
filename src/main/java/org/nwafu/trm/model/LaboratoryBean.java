package org.nwafu.trm.model;

/**
 * 实验室类
 */
public class LaboratoryBean {
    private String labName;
    private String labNum;
    private String labCategory;
    private String labEquipment;
    private String labGrade;
    private String labHead;
    private String labPhone;

    public String getLabName() {
        return labName;
    }

    public void setLabName(String labName) {
        this.labName = labName;
    }

    public String getLabNum() {
        return labNum;
    }

    public void setLabNum(String labNum) {
        this.labNum = labNum;
    }

    public String getLabCategory() {
        return labCategory;
    }

    public void setLabCategory(String labCategory) {
        this.labCategory = labCategory;
    }

    public String getLabEquipment() {
        return labEquipment;
    }

    public String getLabGrade() {
        return labGrade;
    }

    public void setLabGrade(String labGrade) {
        this.labGrade = labGrade;
    }

    public void setLabEquipment(String labEquipment) {
        this.labEquipment = labEquipment;
    }

    public String getLabHead() {
        return labHead;
    }

    public void setLabHead(String labHead) {
        this.labHead = labHead;
    }

    public String getLabPhone() {
        return labPhone;
    }

    public void setLabPhone(String labPhone) {
        this.labPhone = labPhone;
    }
}
