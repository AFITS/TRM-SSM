package org.nwafu.trm.model;

/**
 * 运动场类
 */
public class PlaygroundBean {

    String playgroundId;
    String playgroundName;
    String playgroundKind;
    String playgroundEquipment;     // 设备
    String playgroundAttribute;     // 归属部门
    String playgroundHead;
    String playgroundPhone;
    String playgroundInfo;

    public String getPlaygroundId() {
        return playgroundId;
    }

    public void setPlaygroundId(String playgroundId) {
        this.playgroundId = playgroundId;
    }

    public String getPlaygroundName() {
        return playgroundName;
    }

    public void setPlaygroundName(String playgroundName) {
        this.playgroundName = playgroundName;
    }

    public String getPlaygroundKind() {
        return playgroundKind;
    }

    public void setPlaygroundKind(String playgroundKind) {
        this.playgroundKind = playgroundKind;
    }

    public String getPlaygroundEquipment() {
        return playgroundEquipment;
    }

    public void setPlaygroundEquipment(String playgroundEquipment) {
        this.playgroundEquipment = playgroundEquipment;
    }

    public String getPlaygroundAttribute() {
        return playgroundAttribute;
    }

    public void setPlaygroundAttribute(String playgroundAttribute) {
        this.playgroundAttribute = playgroundAttribute;
    }

    public String getPlaygroundHead() {
        return playgroundHead;
    }

    public void setPlaygroundHead(String playgroundHead) {
        this.playgroundHead = playgroundHead;
    }

    public String getPlaygroundPhone() {
        return playgroundPhone;
    }

    public void setPlaygroundPhone(String playgroundPhone) {
        this.playgroundPhone = playgroundPhone;
    }

    public String getPlaygroundInfo() {
        return playgroundInfo;
    }

    public void setPlaygroundInfo(String playgroundInfo) {
        this.playgroundInfo = playgroundInfo;
    }
}
